import pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.naive_bayes import MultinomialNB
from sklearn.linear_model import LogisticRegression
from sklearn.svm import SVC
from sklearn.metrics import accuracy_score, classification_report, confusion_matrix

df = pd.read_csv('/netflix_titles.csv')

print('Dataset Sample:')
print(df.head())
print(f'Columns: {df.columns}')

df = df[['description', 'listed_in']].dropna()
df['genre'] = df['listed_in'].apply(lambda x: x.split(',')[0].strip())

print(f'Missing values: {df.isnull().sum().sum()}')

X_train, X_test, y_train, y_test = train_test_split(df['description'], df['genre'], test_size=0.2, random_state=42)

tfidf = TfidfVectorizer(stop_words='english', max_features=5000)
X_train_tfidf = tfidf.fit_transform(X_train)
X_test_tfidf = tfidf.transform(X_test)

nb = MultinomialNB()
nb.fit(X_train_tfidf, y_train)
nb_pred = nb.predict(X_test_tfidf)

lr = LogisticRegression(max_iter=1000)
lr.fit(X_train_tfidf, y_train)
lr_pred = lr.predict(X_test_tfidf)

svm = SVC()
svm.fit(X_train_tfidf, y_train)
svm_pred = svm.predict(X_test_tfidf)

def evaluate_model(name, y_true, y_pred):
    print(f'--- {name} Model Evaluation ---')
    print('Accuracy:', accuracy_score(y_true, y_pred))
def evaluate_model(name, y_true, y_pred):
    print(f'--- {name} Model Evaluation ---')
    print('Accuracy:', accuracy_score(y_true, y_pred))
    print('Classification Report:')
    print(classification_report(y_true, y_pred))
    print('Confusion Matrix:\n', confusion_matrix(y_true, y_pred))

evaluate_model('Naive Bayes', y_test, nb_pred)
evaluate_model('Logistic Regression', y_test, lr_pred)
evaluate_model('Support Vector Machine', y_test, svm_pred)